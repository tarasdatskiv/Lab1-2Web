<?php

// Оголошення масивів A та B
$A = array(3, 6, 9, 2, 5, 8, 1, 4, 7);
$B = array(10, 13, 16, 19, 12, 15, 18, 11, 14);

// Знаходження мінімального значення у масиві A
$minA = min($A);

// Знаходження мінімального значення у масиві B
$minB = min($B);

// Обчислення значення виразу (3E+5F)/10
$result = (3 * $minA + 5 * $minB) / 10;

echo "Значення виразу: $result";

?>
