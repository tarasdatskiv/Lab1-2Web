<?php
// Зчитуємо число з клавіатури
$number = readline("Введіть число: ");

// Ініціалізуємо змінну для зберігання суми цифр
$sum = 0;

// Перетворюємо число на рядок, щоб можна було ітеруватись по його цифрам
$numberStr = (string)$number;

// Ітеруємося по кожному символу числа та додаємо його до суми
for ($i = 0; $i < strlen($numberStr); $i++) {
    $sum += (int)$numberStr[$i];
}

// Виводимо суму цифр
echo "Сума цифр числа $number: $sum";
?>
